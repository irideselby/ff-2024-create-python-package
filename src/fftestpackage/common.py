"""A module containing functions that are used throughout the package."""


def multiply_numbers(number_1: float, number_2: float) -> float:
    """Multiply two given numbers.

    Parameters
    ----------
    number_1
        A number to be multiplied.

    number_2
        A number to be multiplied.

    Returns
    -------
        A number calculated from multiplying two numbers.
    """

    return number_1 * number_2


def divide_numbers(number_1: float, number_2: float) -> float:
    """Divide two given numbers.

    Parameters
    ----------
    number_1
        A number to be divided.

    number_2
        A number to be divided by.

    Returns
    -------
        A number calculated from dividing two numbers.
    """

    return number_1 / number_2
